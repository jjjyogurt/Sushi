let currentLocale = DEFAULT_LOCALE;
const listeners = new Set();

function safelyReadStoredLocale() {
  try {
    return window.localStorage.getItem(STORAGE_KEY) || "";
  } catch (_error) {
    return "";
  }
}

function safelyPersistLocale(locale) {
  try {
    window.localStorage.setItem(STORAGE_KEY, locale);
  } catch (_error) {
    // Ignore persistence failures in private browsing or restricted contexts.
  }
}

function resolveLocale(value) {
  const normalized = String(value || "").trim().toLowerCase();
  if (!normalized) {
    return DEFAULT_LOCALE;
  }
  if (normalized.startsWith("zh")) {
    return "zh";
  }
  return SUPPORTED_LOCALES.includes(normalized) ? normalized : DEFAULT_LOCALE;
}

function interpolate(template, variables) {
  if (!variables) {
    return template;
  }
  return Object.entries(variables).reduce((output, [key, value]) => {
    return output.replaceAll(`{${key}}`, String(value));
  }, template);
}

export function t(key, variables) {
  const fallback = TRANSLATIONS[DEFAULT_LOCALE][key] || key;
  const value = TRANSLATIONS[currentLocale][key] || fallback;
  return interpolate(value, variables);
}

export function getLocale() {
  return currentLocale;
}

export function getSupportedLocales() {
  return [...SUPPORTED_LOCALES];
}

export function setLocale(locale, { persist = true } = {}) {
  const nextLocale = resolveLocale(locale);
  if (nextLocale === currentLocale) {
    return currentLocale;
  }
  currentLocale = nextLocale;
  document.documentElement.lang = currentLocale;
  if (persist) {
    safelyPersistLocale(currentLocale);
  }
  listeners.forEach((listener) => {
    listener(currentLocale);
  });
  return currentLocale;
}

export function onLocaleChange(listener) {
  listeners.add(listener);
  return () => {
    listeners.delete(listener);
  };
}

